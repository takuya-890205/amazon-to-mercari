/**
 * ルールベースでカテゴリを推定
 */
function mapCategory(product) {
	const searchText = `${product.title} ${product.brand} ${(product.categoryBreadcrumb || []).join(" ")}`;
	for (const [keyword, category] of Object.entries(KEYWORD_MAPPING)) {
		if (searchText.includes(keyword)) {
			return category;
		}
	}
	if (product.categoryBreadcrumb && product.categoryBreadcrumb.length > 0) {
		return product.categoryBreadcrumb.slice(0, 3);
	}
	return ["その他", "その他", "その他"];
}
